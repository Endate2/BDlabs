SELECT 
    "Н_ЛЮДИ"."ФАМИЛИЯ", 
    "Н_ОБУЧЕНИЯ"."ЧЛВК_ИД", 
    "Н_УЧЕНИКИ"."ГРУППА"
FROM 
    "Н_ЛЮДИ"
LEFT JOIN 
    "Н_ОБУЧЕНИЯ"
    ON "Н_ЛЮДИ"."ИД" = "Н_ОБУЧЕНИЯ"."ЧЛВК_ИД" 
LEFT JOIN 
    "Н_УЧЕНИКИ" 
    ON "Н_ОБУЧЕНИЯ"."ЧЛВК_ИД" = "Н_УЧЕНИКИ"."ЧЛВК_ИД"
WHERE 
    "Н_ЛЮДИ"."ИМЯ" = 'Владимир'
    AND "Н_ОБУЧЕНИЯ"."ЧЛВК_ИД" < 113409;
